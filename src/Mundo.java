    private ArrayList<Pessoa> pessoas;

    public void geraPessoas(){
        ArrayList<Pessoa> pessoas = new ArrayList<>();
        Random rand = new Random();
        for(int i = 0; i < 1; i++){
            pessoas.add(new PessoaBemInformada(rand.nextInt(28)+1, rand.nextInt(58)+1, 56, String.valueOf(i+100)));
        }
        setPessoas(pessoas);
    }

    private void setPessoas(ArrayList<Pessoa> pessoas) {
        this.pessoas = pessoas;
    }

    public int[][] desenhaPessoas(ArrayList<Pessoa> pessoas) {
        int[][] mapa = new int[this.mundo.length][];
        for (int i = 0; i < mapa.length; i++) {
            mapa[i] = new int[mundo[i].length];
            System.arraycopy(mundo[i], 0, mapa[i], 0, mundo[i].length);
        }
        for (Pessoa p : pessoas) mapa[p.getX()][p.getY()] = p.getCor();
        return mapa;
    }

    public void checaPessoas(){
        for(Pessoa p : pessoas)
            if ((mundo[p.getX() + 1][p.getY()] != 0) || (mundo[p.getX()][p.getY() + 1] != 0) || (mundo[p.getX()][p.getY() - 1] != 0) ||
                    (mundo[p.getX() - 1][p.getY()] != 0)) {
                ///////
                break;
            }
    }

